"""
Suma( + )               Modulo( % )
resta( - )              Division ( / )
multiplicacion ( * )    Division entera ( // )
Exponente ( ** )
"""

print("Suma: ")

suma_num1 = 5
suma_num2 = 8
resultado = suma_num1 + suma_num2
print(suma_num1," + ",suma_num2," = ", resultado)

print("Resta: ")

resta_num1 = 5
resta_num2 = 2
resultado = resta_num1 - resta_num2
print(resta_num1," - ",resta_num2," = ", resultado)

print("Multiplicacion: ")
multi_num1 = 5
multi_num2 = 9
resultado = multi_num1 * multi_num2
print(multi_num1," * ",multi_num2," = ", resultado)

print("Exponente: ")

exp_num1 = 2 ** 5
print("2 elevado a 5 es: ", exp_num1)

print("Division: ")

div_num1 = 10
div_num2 = 2
resultado = div_num1 / div_num2
print(div_num1," / ",div_num2," = ", resultado)

print("Modulo(resto): ")

mod_num1 = 10
mod_num2 = 2
resultado = div_num1 % div_num2
print(mod_num1," % ",mod_num2," = ", resultado)

print("Division entera: ")

dive_num1 = 10
dive_num2 = 3
resultado = dive_num1 // dive_num2
print(dive_num1," // ",dive_num2," = ", resultado)
resultado = dive_num1 / dive_num2
print("Resultado sin la division entera: ", resultado)